    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String course = (String)jComboBox1.getSelectedItem();
        String branch = (String)jComboBox2.getSelectedItem();
        String rollNo = jTextField2.getText();
        String name = jTextField3.getText();
        String gender = (String)jComboBox3.getSelectedItem();
        String fathername = jTextField1.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con  = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresultmanagement","root","root");
            
//            Statement st = con.createStatement();
//            st.executeUpdate("insert into student(rollno, course, branch, name, gender, fathername) values('"+rollNo+"', '"+course+"', '"+branch+"', '"+name+"', '"+gender+"', '"+fathername+"')");

            String sql="insert into student(rollno, course, branch, name, gender, fathername) values(?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            
            pst.setString(1, rollNo);
            pst.setString(2,course);
            pst.setString(3, branch);
            pst.setString(4,name);
            pst.setString(5,gender);
            pst.setString(6,fathername);
            
            int updatedRowCount = pst.executeUpdate();
            if(updatedRowCount > 0){
                JOptionPane.showMessageDialog(this, "Record Insertion Succesfull");
            }else{
                JOptionPane.showMessageDialog(this, "Record Insertion failed");
            }
            this.setVisible(false);
            new index().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

